// Задача 2.
// С помощью цикла for вычислите факториал числа (произведение всех чисел от 1 до заданного числа). Используйте prompt для получение начального значения.

let n = +(prompt("Введите число для вычисления факториала:"));
let result = 1;

if (n < 0) {
    console.log("Факториал отрицательного числа не определен.");
} else {
    for (let i = 1; i <= n; i++) {
        result *= i;
    }
    console.log(`Факториал числа ${n} равен: ${result}`);
}


// *Задача 3.
// Выведите в консоль шахматную доску размером 8x8, где символы чередуются (например, # и пробел). Начинайте с # в первой ячейке первой строки.
let size = 8;
let board = "";

for (let row = 0; row < size; row++) {
    for (let col = 0; col < size; col++) {
        if ((row + col) % 2 === 0) {
            board += "#"; // Черная клетка
        } else {
            board += " "; // Белая клетка
        }
    }
    board += "\n";
}

console.log(board);

